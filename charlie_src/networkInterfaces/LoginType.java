package networkInterfaces;

/**
 * Enumerated types representing the different login accounts possible.
 * Each class that extends from networkInterfaces.LoginAccount should contain one
 * of these types.
 * @author Team5
 *
 */
public enum LoginType
{
	SYSTEM_ADMIN,
	OFFICE_MANAGER,
	WAREHOUSE_MANAGER,
	SALES_ASSOCIATE
}
